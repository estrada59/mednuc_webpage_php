                        Es importante su uso especialmente:
                            <li>Niños menores de 1 año.</li>
                            <li>Pacientes transplantados renales.</li>
                            <li>Pacientes con algún grado de Insuficiencia renal.</li>
                            <li>Evaluación de HTA renovascular.</li>
                        </ul>

                        <h4>2.- Gammagrama Renal DTPA:</h4>

                        <p>Es filtrado a nivel glomerular. Hoy en día es una alternativa al uso del MAG3, sólo utilizado dado su menor costo, aunque con la limitante de menor extracción, lo que puede determinar falsos positivos, para algunos casos como estenosis Pieloureteral o NTA falsamente aumentada en riñones transplantados.</p>

                        <p>En el caso de hipertensión renovascular se puede realizar la prueba farmacológica con captopril o furosemide para evaluar la respuesta de las arterias renales o las entidades obstructivas respectivamente.</p>

                        <h4>3.- Gammagrama Renal DMSA</h4>

                        <p>Permite evaluar forma, tamaño y situación renal, si bien la ecografía es mejor para estos parámetros; permite evaluar el parénquima renal funcionante mediante el cálculo de la función renal relativa; y permite detectar lesiones corticales en PNA, que es su gran indicación, tanto en pielonefritis aguda como cicatrices renales.</p>

                        <p>La función renal relativa, o función renal por separado, se obtiene mediante la división de la actividad de cada riñón por separado, medida durante la segunda fase, por la suma de la actividad de ambos.</p>

                        

