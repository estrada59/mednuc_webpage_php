                        <h4>1.- Gammagrama Renal MAG3:</h4>

                        <p>Es básicamente secretado a nivel tubular. Es el fármaco de elección en los estudios renales dinámicos, dada su mayor tasa de extracción renal y por ende tener un patrón más fisiológico que el DTPA. </p>

